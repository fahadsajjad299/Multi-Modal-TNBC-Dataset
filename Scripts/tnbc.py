# CONDITION 7 — HER2 Negative by ASCO/CAP criteria
#
# NOTE: In this TCGA cBioPortal dataset, IHC-HER2 (text) is the primary
# status field. IHC Score is a numeric internal scale and does NOT
# correspond to the standard 0/1+/2+/3+ scoring — do NOT use it for
# HER2 classification here.
#
# Logic:
#   IHC-HER2 = Negative                        → HER2 Negative ✅
#   IHC-HER2 = Equivocal + FISH Negative       → HER2 Negative ✅
#   IHC-HER2 = Equivocal + FISH missing/Pos    → EXCLUDE ❌
#   IHC-HER2 = Positive                        → EXCLUDE ❌
#   IHC-HER2 = missing  + FISH Negative        → HER2 Negative ✅
#   IHC-HER2 = missing  + FISH missing/Pos     → EXCLUDE ❌
# ══════════════════════════════════════════════════════════════════════
df['_ihc']  = df['IHC-HER2'].astype(str).str.lower().str.strip()
df['_fish'] = df['HER2 fish status'].astype(str).str.lower().str.strip()

def her2_negative(row):
    ihc  = row['_ihc']
    fish = row['_fish']
    FISH_NEG = {'negative', 'non-amplified', 'not amplified'}

    if ihc == 'negative':
        return True
    if ihc in ('equivocal', 'indeterminate'):
        return fish in FISH_NEG
    if ihc == 'positive':
        return False
    # IHC missing — fall back to FISH
    if ihc in ('nan', 'missing', ''):
        return fish in FISH_NEG
    return False  # catch-all

mask = df.apply(her2_negative, axis=1)
log_exclusion(df[~mask]['Patient ID'].tolist(),
              'C07: HER2 not confirmed Negative by ASCO/CAP IHC + FISH criteria')
df = df[mask]
