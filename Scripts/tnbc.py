# ── Helper functions ─────────────────────────────────────────────────────
def val_is(series, *values):
    """Case-insensitive exact match."""
    return series.astype(str).str.lower().str.strip().isin([v.lower() for v in values])

def val_contains(series, substr):
    """Case-insensitive substring match."""
    return series.astype(str).str.lower().str.contains(substr.lower(), na=False)

